
//Java program for swapping two numbers
package com.globallogic.demo;

public class Swapping {
	public static void main(String[] args) {
		int num1=10;
		int num2=11;
		num1= num1+num2;
		num2=num1-num2;
		num1=num1-num2;
		System.out.println("new num1 is "+ num1 + "\n" +"new num2 is " +num2);
	}

}
