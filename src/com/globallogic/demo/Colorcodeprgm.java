public class Colorcodeprgm {
	public static void main(String[] args) {
		int color = 2;
        switch(color){
            case 1: System.out.println("Red");
                    break;
            case 2: System.out.println("Yellow");
                    break;
            case 3: System.out.println("Green");
                    break;
            default: System.out.println("No Color Detected"); 
        }
        }
}

